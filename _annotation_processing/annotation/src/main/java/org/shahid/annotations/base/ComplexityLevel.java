package org.shahid.annotations.base;

/**
 * Enumeration of complexity levels.
 *
 * @author Shaikh Shahid Iqbal
 */
public enum ComplexityLevel {

    /**
     * Complexity very simple.
     */
    VERY_SIMPLE,

    /**
     * Complexity simple.
     */
    SIMPLE,

    /**
     * Complexity medium.
     */
    MEDIUM,

    /**
     * Complexity complex.
     */
    COMPLEX,

    /**
     * Complexity very complex.
     */
    VERY_COMPLEX;
}
